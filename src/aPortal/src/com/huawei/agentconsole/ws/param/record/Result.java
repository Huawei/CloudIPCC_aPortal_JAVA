
package com.huawei.agentconsole.ws.param.record;


public class Result
{

    private String callerId;
    
    private String calledId;
    
    private String agentId;
    
    private String callType;
    
    private String beginTime;
    
    private String endTime;
    
    private String callTimeCic;
    
    private String fileName;

    /**
     * @return the callerId
     */
    public String getCallerId()
    {
        return callerId;
    }

    /**
     * @param callerId the callerId to set
     */
    public void setCallerId(String callerId)
    {
        this.callerId = callerId;
    }

    /**
     * @return the calledId
     */
    public String getCalledId()
    {
        return calledId;
    }

    /**
     * @param calledId the calledId to set
     */
    public void setCalledId(String calledId)
    {
        this.calledId = calledId;
    }

    /**
     * @return the agentId
     */
    public String getAgentId()
    {
        return agentId;
    }

    /**
     * @param agentId the agentId to set
     */
    public void setAgentId(String agentId)
    {
        this.agentId = agentId;
    }

    /**
     * @return the callType
     */
    public String getCallType()
    {
        return callType;
    }

    /**
     * @param callType the callType to set
     */
    public void setCallType(String callType)
    {
        this.callType = callType;
    }

    /**
     * @return the beginTime
     */
    public String getBeginTime()
    {
        return beginTime;
    }

    /**
     * @param beginTime the beginTime to set
     */
    public void setBeginTime(String beginTime)
    {
        this.beginTime = beginTime;
    }

    /**
     * @return the endTime
     */
    public String getEndTime()
    {
        return endTime;
    }

    /**
     * @param endTime the endTime to set
     */
    public void setEndTime(String endTime)
    {
        this.endTime = endTime;
    }

    /**
     * @return the callTimeCic
     */
    public String getCallTimeCic()
    {
        return callTimeCic;
    }

    /**
     * @param callTimeCic the callTimeCic to set
     */
    public void setCallTimeCic(String callTimeCic)
    {
        this.callTimeCic = callTimeCic;
    }

    public String getFileName()
    {
        return fileName;
    }

    public void setFileName(String fileName)
    {
        this.fileName = fileName;
    }
    
    
}
